int	ft_isalnum(int l)
{
	if (l >= '0' && l <= '9')
	{
		return (1);
	}
	if ((l >= 'a' && l <= 'z') || (l >= 'A' && l <= 'Z'))
	{
		return (1);
	}
	return (0);
}
